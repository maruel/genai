// wsConn abstracts a WebSocket text-message connection for recording/replay.
type wsConn interface {
	Send(data string) error
	Receive(msg *string) error
	Close() error
}

// websocketConn adapts *websocket.Conn to wsConn.
type websocketConn struct {
	*websocket.Conn
}

func (c *websocketConn) Send(data string) error {
	return websocket.Message.Send(c.Conn, data)
}

func (c *websocketConn) Receive(msg *string) error {
	return websocket.Message.Receive(c.Conn, msg)
}

// WebSocketConn is a persistent WebSocket connection to the OpenAI Responses API.
//
// It implements genai.Provider and io.Closer. Each GenSync/GenStream call sends a
// response.create message and collects the streaming response. Responses are
// automatically chained via previous_response_id for lower latency on multi-turn
// tool-call loops.
//
// The connection tracks previously sent messages via Reply.Opaque metadata.
// On subsequent calls, only new messages (the delta) are sent to the server,
// since it already has the prior conversation via previous_response_id.
//
// Key constraints:
//   - One response in-flight at a time (no multiplexing)
//   - 60-minute connection limit
//   - Compatible with store=false and ZDR
//
// Create via Client.WebSocket().
//
// https://developers.openai.com/api/docs/guides/websocket-mode
type WebSocketConn struct {
	base.NotImplemented

	client *Client

	mu sync.Mutex
	// Protected by mu.
	ws wsConn
}

// WebSocket opens a persistent WebSocket connection to the OpenAI Responses API.
//
// The returned connection inherits the client's model, API key, and base URL.
// Call Close() when done.
func (c *Client) WebSocket(ctx context.Context) (*WebSocketConn, error) {
	if c.impl.Model == "" {
		return nil, errors.New("a model is required")
	}
	// Derive WebSocket URL from the client's base URL.
	wsURL := strings.Replace(c.baseURL, "https://", "wss://", 1)
	wsURL = strings.Replace(wsURL, "http://", "ws://", 1)
	wsURL += "/responses"

	wsCfg, err := websocket.NewConfig(wsURL, wsURL)
	if err != nil {
		return nil, fmt.Errorf("failed to create websocket config: %w", err)
	}
	// Extract auth headers from the HTTP client's transport chain.
	wsCfg.Header = http.Header{}
	wsCfg.Header.Set("OpenAI-Beta", "responses=v1")
	if h, ok := c.impl.ProviderBase.Client.Transport.(*roundtrippers.Header); ok {
		for k, vs := range h.Header {
			for _, v := range vs {
				wsCfg.Header.Set(k, v)
			}
		}
	}
	raw, err := wsCfg.DialContext(ctx)
	if err != nil {
		return nil, fmt.Errorf("failed to connect to websocket %s: %w", wsURL, err)
	}
	return &WebSocketConn{
		client: c,
		ws:     &websocketConn{raw},
	}, nil
}

