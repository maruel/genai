// Leverage the content.jpg file generated in txt_to_img example to ask
// gemini-2.5-flash-image-preview to change the image with a prompt and ask
// the model to explain what it did.
//
// This requires `GEMINI_API_KEY` (https://aistudio.google.com/apikey)
// environment variable to authenticate.

package main

import (
	"context"
	"fmt"
	"io"
	"log"
	"net/http"
	"os"

	"github.com/maruel/genai"
	"github.com/maruel/genai/providers/gemini"
)

func main() {
	ctx := context.Background()
	// Warning: This is a bit expensive.
	c, err := gemini.New(ctx,
		genai.ProviderOptionModel("gemini-2.5-flash-image-preview"),
		genai.ProviderOptionModalities{genai.ModalityImage, genai.ModalityText})
	if err != nil {
		log.Fatal(err)
	}
	// Reuse the image generated by example txt_to_img.
	f, err := os.Open("content.jpg")
	if err != nil {
		log.Fatal(err)
	}
	defer f.Close()
	msgs := genai.Messages{
		genai.Message{Requests: []genai.Request{
			{Text: "I'd like you to generate a new image and add more animals in it. Tell me which animals you added and why."},
			{Doc: genai.Doc{Src: f}},
		}},
	}
	res, err := c.GenSync(ctx, msgs, &gemini.GenOption{ThinkingBudget: 0})
	if err != nil {
		log.Fatal(err)
	}
	for _, r := range res.Replies {
		if r.Doc.IsZero() {
			fmt.Println(r.Text)
			continue
		}
		// The image can be returned as an URL or inline, depending on the provider.
		var src io.Reader
		if r.Doc.URL != "" {
			req, err := c.HTTPClient().Get(r.Doc.URL)
			if err != nil {
				log.Fatal(err)
			} else if req.StatusCode != http.StatusOK {
				log.Fatal(req.StatusCode)
			}
			src = req.Body
			defer req.Body.Close()
		} else {
			src = r.Doc.Src
		}
		b, err := io.ReadAll(src)
		if err != nil {
			log.Fatal(err)
		}
		name := r.Doc.GetFilename()
		fmt.Printf("Wrote: %s\n", name)
		if err = os.WriteFile(name, b, 0o644); err != nil {
			log.Fatal(err)
		}
	}
}
